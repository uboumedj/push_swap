#include "../../inc/libft.h"

char	*ft_strdup(const char *s)
{
	char	*res;
	size_t	len;
	int		i;

	len = ft_strlen(s);
	if (!(res = (char *)malloc(sizeof(char) * len + 1)))
	{
		return (NULL);
	}
	i = 0;
	while (s[i] != '\0')
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
