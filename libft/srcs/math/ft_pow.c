int		ft_pow(int x, int y)
{
	int i;
	int res;

	if (y < 0)
		return (0);
	else if (y == 0)
		return (1);
	i = 1;
	res = x;
	while (i < y)
	{
		res *= res;
		i++;
	}
	return (res);
}
